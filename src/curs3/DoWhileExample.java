package curs3;

public class DoWhileExample {

	public static void main(String[] args) {
	
		boolean flag=false;
		
		String[] array = {"one", "two", "three", "four"};
		int j=0;
		while(flag==true) {
			System.out.println(array[j]);
			j++;
	
		
		
		
		}
		System.out.println("-------------------");
		
		
		int x=0;
		do { 
			System.out.println(array[x]);
			x++;
			
		}while(flag==true);
	

	}

}
